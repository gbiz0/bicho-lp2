
package br.com.lp2.dto;

public class BichoDTO {
    private String nome_cli, bairro_cli, cidade_cli, estado_cli, cpf_cli, telefone_cli, nome_bicho, tipo_aposta;
    private int id_cli, numero_cli;
    private float valor_aposta;

    public String getNome_cli() {
        return nome_cli;
    }

    public void setNome_cli(String nome_cli) {
        this.nome_cli = nome_cli;
    }

    public String getBairro_cli() {
        return bairro_cli;
    }

    public void setBairro_cli(String bairro_cli) {
        this.bairro_cli = bairro_cli;
    }

    public String getCidade_cli() {
        return cidade_cli;
    }

    public void setCidade_cli(String cidade_cli) {
        this.cidade_cli = cidade_cli;
    }

    public String getEstado_cli() {
        return estado_cli;
    }

    public void setEstado_cli(String estado_cli) {
        this.estado_cli = estado_cli;
    }

    public String getCpf_cli() {
        return cpf_cli;
    }

    public void setCpf_cli(String cpf_cli) {
        this.cpf_cli = cpf_cli;
    }

    public String getTelefone_cli() {
        return telefone_cli;
    }

    public void setTelefone_cli(String telefone_cli) {
        this.telefone_cli = telefone_cli;
    }

    public String getNome_bicho() {
        return nome_bicho;
    }

    public void setNome_bicho(String nome_bicho) {
        this.nome_bicho = nome_bicho;
    }

    public String getTipo_aposta() {
        return tipo_aposta;
    }

    public void setTipo_aposta(String tipo_aposta) {
        this.tipo_aposta = tipo_aposta;
    }
    public int getId_cli() {
        return id_cli;
    }

    public void setId_cli(int id_cli) {
        this.id_cli = id_cli;
    }

    public int getNumero_cli() {
        return numero_cli;
    }

    public void setNumero_cli(int numero_cli) {
        this.numero_cli = numero_cli;
    }

    public float getValor_aposta() {
        return valor_aposta;
    }

    public void setValor_aposta(float valor_aposta) {
        this.valor_aposta = valor_aposta;
    }
    
    
}
